#addition
addition=lambda n1,n2:n1+n2
print(addition(10,20))

#subtract
subtract=lambda n1,n2:n1-n2
print(subtract(20,10))

#multiplication
multiplication=lambda n1,n2:n1*n2
print(multiplication(10,10))

#division

division=lambda n1,n2:n1/n2
print(division(15,3))

#square

square=lambda n:n**2
print(square(2))

#odd,even
numcheck=lambda num:"even" if num%2==0 else "odd"
print(numcheck(10))